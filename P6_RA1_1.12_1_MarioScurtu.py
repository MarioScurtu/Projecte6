# Descripció: Calcula l’àrea d’un cercle a partir del radi.  
# Especificacions de l'entrega: L’usuari introdueix el radi i es mostra l’àrea amb PI = 3.1416.

PI = 3.1416

print ("Introdueix el radi del cercle: ")
radi = int(input())
area = PI * (radi ** 2)

print("L’àrea del cercle és:", area)
