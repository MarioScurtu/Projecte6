# Descripció: Converteix graus Celsius a Fahrenheit.  
# Especificacions de l'entrega: L’usuari introdueix la temperatura en ºC i el programa la mostra en ºF.

print ("Introdueix la temperatura en graus Celsius: ")
celsius = int(input())
fahrenheit = (celsius * 9/5) + 32
print("La temperatura en Fahrenheit és:", fahrenheit)
