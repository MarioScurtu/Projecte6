# Descripció:
#   Indica si una nota de l'1 al 10 és aprovada o suspesa.
#
# Especificacions de l'entrega:
#   Fes un programa que et demani un número de l’1 al 10 i segons la nota et digui:
#   “Aprovat” o “Suspès”.
