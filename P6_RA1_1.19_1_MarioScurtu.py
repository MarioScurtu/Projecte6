# Descripció: Calcula la superfície d’una habitació.  
# Especificacions de l'entrega: L’usuari introdueix llargada i amplada i es calcula la superfície.

print ("Introdueix la llargada (en metres): ")
llargada = float(input())

print ("Introdueix l'amplada (en metres): ")
amplada = float(input())

superficie = llargada * amplada
print("La superfície de l'habitació és:", superficie, "m²")