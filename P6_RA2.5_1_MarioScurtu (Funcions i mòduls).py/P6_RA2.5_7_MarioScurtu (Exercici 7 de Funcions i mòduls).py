# Descripció:
#   Troba el nombre més gran entre tres nombres donats.
#
# Especificacions d'entrada:
#   - Tres nombres a comparar.
# Especificacions de sortida:
#   - Retorna el nombre més gran.
# ================================================================

def maxim(a, b, c):
    return max(a, b, c)

# Crides a la funció
print(maxim(3, 7, 5))
print(maxim(10, 2, 8))
print(maxim(1, 1, 1))
