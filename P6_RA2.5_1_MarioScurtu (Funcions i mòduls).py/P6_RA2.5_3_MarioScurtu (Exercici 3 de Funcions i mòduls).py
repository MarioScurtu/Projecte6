# Descripció:
#   Calcula l'àrea de diferents rectangles amb una funció.
#
# Especificacions d'entrada:
#   - Base i altura predefinides dins del codi.
# Especificacions de sortida:
#   - Mostra per pantalla l'àrea de cada rectangle.
# ================================================================

def area_rectangle(base, altura):
    return base * altura

print("Àrea de 3x4:", area_rectangle(3, 4))
print("Àrea de 5x10:", area_rectangle(5, 10))
print("Àrea de 8x2:", area_rectangle(8, 2))
