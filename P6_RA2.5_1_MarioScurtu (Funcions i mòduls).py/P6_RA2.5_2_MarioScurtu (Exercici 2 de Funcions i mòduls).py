# Descripció:
#   Defineix una funció que suma dos nombres i imprimeix el resultat
#   de tres crides amb diferents valors.
#
# Especificacions d'entrada:
#   - No hi ha entrada de l'usuari, els valors són fixos dins del codi.
# Especificacions de sortida:
#   - Mostra per pantalla el resultat de cada suma.
